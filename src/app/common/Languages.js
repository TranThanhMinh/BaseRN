  async loadLanguage() {
    let lang = await AsyncStorage.getItem(Constants.Store.AppLang);
    // this.strings.setLanguage(lang ? lang : 'ja');
    // this.strings.setLanguage('zhs');
    global.lang = lang != null ? lang : 'ja';
  }

  replaceAll(str, find, replace) {
    return str.replace(new RegExp(find, 'g'), replace);
  }

  renderColorText(text) {
    return <Text style={[{}, text.includes('_fontBold') && { fontWeight: 'bold' }]}>{text.replace(/<\/?[^>]+(>|$)/g, "")}</Text>
  }

  renderNonColorText(text) {
    return <Text style={[{}, text.includes('_fontBold') && { fontWeight: 'bold' }]}>{text}</Text>
  }

  // 本作業による決済金額は以下の通りです。\n決済金額：<c color=\"red\" fontStyle=\"bold\">{total}円</c>（税抜き）- {included_total}円（税込み）\n承認後はキャンセルできませんので、ご注意ください。
  renderMultiTextStyle(message) {
    let strings = message.replaceAll("< c", "<c").replaceAll("c >", "c>").replaceAll("<c", "<_><c").replaceAll("c>", "c><_>").split('<_>')
    let texts = []
    strings.forEach(item => {
      if (item.includes("<c")) {
        texts.push(this.renderColorText(item))
      } else {
        texts.push(this.renderNonColorText(item))
      }
    });
    return texts
  }

  getStyle(key, params = {}) {
    let message = this.get(key, params)
    return this.renderMultiTextStyle(message)
  }

  get(key, params = {}) {
    let lang = 'ja'; //global.lang != null ? global.lang : 'ja';
    if (this.strings[lang]) {
      let temp = this.strings[lang][key];
      if ((temp == null || temp == "") && !__DEV__) {
        temp = this.strings.ja[key]
      }
      if (temp == null || temp == "") {
        temp = key
        Logging.log(temp)
      } else {
        for (const prop in params) {
          temp = this.replaceAll(temp, `{${prop}}`, params[prop] ? params[prop] : "");
        }
      }
      if (temp == null) {
        temp = ""
      }
      return temp.trim();
    } else {
      return key;
    }
  }
